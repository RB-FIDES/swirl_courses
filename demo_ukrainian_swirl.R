# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —à–≤–∏–¥–∫–æ–≥–æ —Ç–µ—Å—Ç—É–≤–∞–Ω–Ω—è
quick_test <- function() {
  cat("–®–≤–∏–¥–∫–∏–π —Ç–µ—Å—Ç —Ñ—Ä–∞–∑:\n")
  cat("Quick phrases test:\n")
  cat("–ü–æ—Ö–≤–∞–ª–∞:", praise_ua(), "\n")
  cat("–°–ø—Ä–æ–±—É–π —â–µ —Ä–∞–∑:", tryAgain_ua(), "\n")
