# –§—É–Ω–∫—Ü—ñ—è –ø–µ—Ä–µ–≤—ñ—Ä–∫–∏ —Å—Ç–∞—Ç—É—Å—É
check_status <- function() {
  if(exists("praise", envir=asNamespace("swirl"))) {
    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ –Ω–∞—à–∞ —Ñ—É–Ω–∫—Ü—ñ—è –∞–∫—Ç–∏–≤–Ω–∞
    test_praise <- get("praise", envir=asNamespace("swirl"))
    if(identical(test_praise, praise_ua)) {
      cat("‚úì –£–∫—Ä–∞—ó–Ω—Å—å–∫—ñ —Ñ—Ä–∞–∑–∏ –ê–ö–¢–ò–í–ù–Ü\n")
      cat("‚úì Ukrainian phrases ACTIVE\n")
    } else {
      cat("‚úó –ê–Ω–≥–ª—ñ–π—Å—å–∫—ñ —Ñ—Ä–∞–∑–∏ –∞–∫—Ç–∏–≤–Ω—ñ\n") 
      cat("‚úó English phrases active\n")
    }
  }
}

cat("\n–î–æ—Å—Ç—É–ø–Ω—ñ –∫–æ–º–∞–Ω–¥–∏:\n")
